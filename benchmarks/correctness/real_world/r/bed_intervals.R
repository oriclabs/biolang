library(GenomicRanges)
library(jsonlite)
bed <- read.table("real_data/ecoli_genes.bed", sep = "\t", stringsAsFactors = FALSE)
colnames(bed)[1:3] <- c("chrom", "start", "end")
n <- nrow(bed)
total_span <- sum(bed$end - bed$start)
chrom_counts <- as.list(table(bed$chrom))
chrom_counts <- lapply(chrom_counts, as.integer)
gr <- GRanges(seqnames = bed$chrom, ranges = IRanges(start = bed$start + 1, end = bed$end))
merged <- reduce(gr)
merged_count <- length(merged)
cat(toJSON(list(n_intervals = n, total_span = total_span, per_chromosome = chrom_counts, merged_count = merged_count), auto_unbox = TRUE))
cat("\n")
